<h1>Lawbrador</h1>

Lawbrador wants to make the law accessible for everyone. We are creating a platform from which you
can access any legal resource, do your research and share it. Lawbrador is in the early stages of
development. Many features that we would like to have are not yet there and bugs may occur
regularly. Please contact<a href="mailto:lennartvanlaake@gmail.com">me</a> if you encounter a bug or
would like to comment on the application.

<h2>How it works</h2>

Instead of saving the legal universe to a private database, Lawbrador passes on the search results
of official legal sources. Based on a configuratble set of rules Lawbrador reformats the original
page into a generic format. Then Lawbrador allows you to research that text any way you want. This
means that all the results you see in Lawbrador are directly imported from their source. By clicking
a link at the top of a results page you can view the original document. At this moment we only
support searching Eurlex, the official source for EU law, with a limited number of search
parameters. In the future we will expand the number of sources and available search parameters.

